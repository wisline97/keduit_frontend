/*
	[1] 변수(variable)
		(1) 변수란, 단 하나의 값을 저장할 수 있는 메모리 공간이다.				
		(2) 변수의 선언 : 메모리 공간 할당 
			var a;						
		(3) 변수의 초기화 : 값 저장
			a = 10;
					
	[2] 변수의 값대입 축약형 
		(1) 변수는 선언 과 값저장이 나눠진다. 
			var a;
			a = 10;
			
		(2) 축약형을 사용하면 위의 식을 한줄로 표현할수있다. 
			var b = 20;	
 */
var num;
num = 10;
console.log(num);

// 변수의 선언과 값대입을 한번에 할수있다.
var num2 = 20;
console.log(num2);

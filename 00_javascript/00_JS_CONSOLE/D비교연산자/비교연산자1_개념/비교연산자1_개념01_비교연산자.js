/*
	[1] 비교 연산자 
		- 비교 연산자의 결과는 참(true) 또는 거짓(false)이다.
		- 아래 비교의 기준은 왼쪽을 기준으로할때
		(1) 크니? 		    10 > 3
		(2) 작니?			10 < 3
		(3) 같니?			10 == 3
		(4) 다르니?		    10 != 3
		(5) 크거나 같니?	10 >= 3
		(6) 작거나 같니?	10 <= 3
 */

/*
		모두 true 가 되도록 숫자를 변경해보세요.
	*/
console.log(10 == 3);
console.log(3 != 3);
console.log(5 > 38);
console.log(12 < 3);
console.log(1 >= 11);

'''
[문제]
	130 의 약수 중에서 작은 수부터 큰 수를 출력했을 때, 
	다섯 번째 약수만 출력하시오.
[정답]
	13
'''
print("문제1")
min = 1
max = 130
count = 0

while min <= 130:
    if max % min == 0:
        count += 1
        if count == 5:
            print("정답",min)
    min += 1


'''
[문제]
    두 자릿수의 28의 배수 중에서 가장 큰 수를 출력하시오.
[정답]
   84
'''
print("문제2")
i = 1
max = 0

while i < 100:
    if i % 28 == 0:
        if max < i:
            max = i

    i += 1

print("정답",max)


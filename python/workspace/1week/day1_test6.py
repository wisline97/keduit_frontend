'''
	[문제]
		사과 15개의 무게를 재었더니3kg 이었습니다. 	 	
	 	사과 3개 의 무게를 구하시오.
	 	소수점 두자리까지 구하시오.
	 	
	 [정답] 
	 	0.6
'''
'''
	15개 : 3kg = 1개 : nkg
	15 x n = 3 x 1
	15n = 3
	15n / 15 = 3 / 15
	n = 3 / 15
'''


print( round(3 / 15 * 3 , 2))

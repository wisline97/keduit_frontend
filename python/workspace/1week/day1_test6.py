'''
	[예시 문제]
		총 3과목의 시험을 보았다.
	  국어는 84 점 수학 23점 과학은 53 점을 받았다.
	  평균을 구하시오. 
	  소수점 두자리까지 구하시오. 
	  	
	[정답] 
		53.33
'''

# 반올림 : round
# round( 실수 , 자리수 )

print((84 + 23 + 53) / 3)

print(round((84 + 23 + 53) / 3, 2))


/*
	[1] 이스케이프 문자(escape sequence)
		문자열 출력 중간에 이스케이프 문자를 사용해서 다양한 효과를 표현할수있다. 
		
	[2] 이스케이프 문자 종류 
		(1) \n	: 줄바꿈
		(2) \t	: tab (4칸 띄어쓰기)
		(3) \"	: "
		(4) \'	: '
		(5) \\	: \
 */

console.log("안녕\n하세요");
console.log("안녕\t하세요");
console.log("'안녕하세요'");
console.log('"안녕하세요"');
console.log("\\안녕하세요\\");

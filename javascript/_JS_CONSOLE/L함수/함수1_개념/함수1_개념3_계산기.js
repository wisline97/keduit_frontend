
function plus(a, b) {  // a 와 b 를 매개 변수라 한다. 
    var c = a + b
    console.log(c);
}
function minus(a, b) {  // a 와 b 를 매개 변수라 한다. 
    var c = a - b
    console.log(c);
}
function multiply(a, b) {  // a 와 b 를 매개 변수라 한다. 
    var c = a * b
    console.log(c);
}
function division(a, b) {  // a 와 b 를 매개 변수라 한다. 
    var c = a / b
    console.log(c.toFixed(2));
}
plus(10, 3);
minus(10, 3);
multiply(10, 3);
division(10, 3);

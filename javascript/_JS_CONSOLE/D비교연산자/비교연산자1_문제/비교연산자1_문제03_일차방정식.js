/*
		[미지수가 양쪽에 있는 일차방정식]
        다음방정식의 풀이과정을 주석으로 표현하시오.
		
    */
/* 
[1]
5a - 2 = 3(4*a+4)
5a - 2 = 12a + 12
5a - 12a = 12 + 2
-7a = 14
a = -2
*/
// [1]
var a = -2;
console.log(5 * a - 2 == 3 * (4 * a + 4));

/* 
[2]
-4 * b + 3 = -2 * (b - 2)
-4b + 3 = -2b + 4
-2b = 4 - 3
2b = -1
b = - 1/ 2
*/
// [2]
var b = -1 / 2;
console.log(-4 * b + 3 == -2 * (b - 2));

/* 
[3]
-c - 1 = -3 * (2 * c + 4)
-c - 1 = -3(2c + 4)
-c - 1 = -6c - 12
-c + 6c = -12 + 1
5c = - 11
c = -11 / 5
*/
// [3]
var c = -11 / 5;
console.log(-c - 1 == -3 * (2 * c + 4));

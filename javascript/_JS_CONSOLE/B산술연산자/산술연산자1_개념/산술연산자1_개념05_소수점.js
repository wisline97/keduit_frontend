/*
        Math.ceil(x): 주어진 값에 소수점 올림하여 정수를 반환
        Math.floor(x): 주어진 값에 소수점 내림하여 정수를 반환
        Math.round(x): 주어진 값에 소수점 반올림하여 정수를 반환
    */

var x1 = 10 / 4;
console.log(x1);

var x2 = 10 / 4;
x2 = Math.ceil(x2);
console.log(x2);

var x3 = 10 / 4;
x3 = Math.floor(x3);
console.log(x3);

var x4 = 10 / 4;
x4 = Math.round(x4);
console.log(x4);

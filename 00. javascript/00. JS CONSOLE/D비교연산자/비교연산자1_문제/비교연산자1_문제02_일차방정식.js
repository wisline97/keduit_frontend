/*
		[미지수가 하나인 일차방정식]
		각 방정식의 풀이과정을 주석으로 표현하시오.
    */

/* 
[1]
7 * 2 - a = 3 * -2 - -3
14 - a = -3
-a = -3 - 14
a = 17
*/
// [1]
var a = 17;
console.log(7 * 2 - a == 3 * -2 - -3);

/* 
[2]
b * 5 - 25 = 0
5b - 25 = 0
5b = 25
b = 5
*/
// [2]
var b = 5;
console.log(b * 5 - 25 == 0);

/* 
[3]
3c + 2 = 20
3c = 18
c = 6
*/
// [3]
var c = 6;
console.log(3 * c + 2 == 20);

/* 
[4]
5d = 20
d = 4
*/
// [4]
var d = 4;
console.log(5 * d == 2 * 10);

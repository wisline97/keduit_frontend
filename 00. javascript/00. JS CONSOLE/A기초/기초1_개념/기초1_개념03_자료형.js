// 자료형(Data Type)
// 1. 숫자(정수) : 소수점이 없는 수 : 10 , -1  , 123123
console.log("[정수]");
console.log(10);
console.log(-1);
console.log(123123);
// 2. 숫자(실수) : 소수점이 있는 수 : 3.14 , -10.3
console.log("[실수]");
console.log(3.14);
console.log(-10.3);
// 3. 문자 : 쌍따옴표나 홑따옴표 2개로 감싼다
console.log("[문자]");
console.log("홑따옴표");
console.log("쌍따옴표");

// 4. 참과 거짓
console.log("[참과 거짓]");
console.log(true);
console.log(false);

// 타입확인하기
console.log("[타입확인]");
console.log(typeof 10); // number
console.log(typeof 10.3); // number
console.log(typeof "10.3"); // string
console.log(typeof false); // boolean
